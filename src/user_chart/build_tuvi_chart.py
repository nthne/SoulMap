def get_ban_menh(lunar_year):
    menh_element, menh_name = BAN_MENH[lunar_year]

    return {
        "element": menh_element,
        "name": menh_name
    }


def get_cung_an_menh(lunar_month, lunar_hour):
    month_index = (lunar_month - 1) % 12

    start_index = month_index

    steps_back = HOUR_INDEX[lunar_hour]

    menh_index = (start_index - steps_back) % 12
    
    return ZODIAC_ORDER[menh_index]


def get_can_group_index(can):
    for i, group in enumerate(CAN_GROUPS):
        if can in group:
            return i


def get_cuc(cung_an_menh, can):
    for zodiac_group, row in CUC_TABLE.items():
        if cung_an_menh in zodiac_group:
            can_idx = get_can_group_index(can)
            cuc_idx = row[can_idx]

            element, name, number = CUC_VALUES[cuc_idx]

            return {
                "element": element,
                "name": name,
                "number": number
            }


def get_menh_chu(cung_an_menh):

    return MENH_CHU_TABLE[cung_an_menh]


def get_than_chu(lunar_year_str):
    chi = lunar_year_str.split()[1]

    return THAN_CHU_TABLE[chi]


def get_am_duong_thuan_nghich(am_duong_gender, cung_an_menh):
    year_ad = am_duong_gender.split()[0]
    if cung_an_menh in DUONG_CHI:
        menh_ad = "Dương"
    else:
        menh_ad = "Âm"

    if year_ad != menh_ad:
        return "Nghịch lý"
    else:
        return "Thuận lý"
    

def get_menh_khac(menh_element):
    
    return f"{menh_element} khắc {KHAC[menh_element]}"


def get_than_cu(lunar_hour):
    
    return THAN_CU_MAP[lunar_hour]


def build_tuvi_chart(user):
    lunar = user["dob_lunar"]

    lunar_year = lunar["year"]
    lunar_month = lunar["month"]
    lunar_hour = lunar["hour"]

    # Tách can
    can = lunar_year.split()[0]

    # 1. Bản mệnh
    ban_menh = get_ban_menh(lunar_year)

    # 2. Cung an mệnh
    cung_an_menh = get_cung_an_menh(lunar_month, lunar_hour)

    # 3. Cục
    cuc = get_cuc(cung_an_menh, can)

    # 4. Mệnh chủ
    menh_chu = get_menh_chu(cung_an_menh)

    # 5. Thân chủ
    than_chu = get_than_chu(lunar_year)

    # 6. Âm dương thuận nghịch
    am_duong = get_am_duong_thuan_nghich(
        user["am_duong_gender"],
        cung_an_menh
    )

    # 7. Mệnh - cục sinh khắc
    menh_cuc = get_menh_khac(ban_menh["element"])

    # 8. Thân cư
    than_cu = get_than_cu(lunar_hour)

    return {
        "chart_id": "chart_456",    # placeholder
        "user_id": user["user_id"],
        "ban_menh": ban_menh,
        "cung_an_menh": cung_an_menh,       # thêm để lập charthouses
        "cuc": {
            "element": cuc["element"],
            "name": cuc["name"],
            "number": cuc["number"]
        },
        "menh_chu": menh_chu,
        "than_chu": than_chu,
        "am_duong_thuan_nghich": am_duong,
        "menh_cuc_sinh_khac": menh_cuc,
        "than_cu": than_cu
    }